from __future__ import annotations

import argparse
import json
import random
import sys
from pathlib import Path

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt
from matplotlib.ticker import PercentFormatter

PROMPT_CASE_ORDER = ["correct", "empty", "shuffled_words", "swapped_prompts"]
PROMPT_CASE_LABELS = {
    "correct": "Correct",
    "empty": "Empty",
    "shuffled_words": "Shuffled Words",
    "swapped_prompts": "Swapped Prompts",
}
RATIO_METRIC = ("compressed_to_initial_ratio", "Compressed Size / Initial Size")


def parse_args(argv):
    parser = argparse.ArgumentParser(
        description=(
            "Plot prompt-ablation result metrics by prompt case from the JSON arrays "
            "generated by run_diffusiondb_prompt_ablation.py."
        )
    )
    parser.add_argument(
        "--results-dir",
        default="results",
        type=str,
        help="Directory containing the prompt-ablation result JSON files.",
    )
    parser.add_argument(
        "--experiment-name",
        default="diffusiondb_prompt_ablation",
        type=str,
        help="Experiment prefix used when the result JSON files were written.",
    )
    parser.add_argument(
        "--all-cases-json",
        default=None,
        type=str,
        help=(
            "Optional explicit path to the combined *_all_cases.json file. "
            "If omitted, the script loads it from --results-dir and --experiment-name."
        ),
    )
    parser.add_argument(
        "--output",
        default=None,
        type=str,
        help="Optional explicit output PNG path.",
    )
    parser.add_argument(
        "--dpi",
        default=200,
        type=int,
        help="Output figure DPI.",
    )
    parser.add_argument(
        "--seed",
        default=42,
        type=int,
        help="Random seed for deterministic point jitter.",
    )
    return parser.parse_args(argv)


def load_case_results(args) -> dict[str, list[dict]]:
    if args.all_cases_json is not None:
        all_cases_path = Path(args.all_cases_json)
    else:
        all_cases_path = Path(args.results_dir) / f"{args.experiment_name}_all_cases.json"

    if all_cases_path.is_file():
        with all_cases_path.open("r", encoding="utf-8") as handle:
            data = json.load(handle)
        return {str(key): list(value) for key, value in data.items()}

    results_dir = Path(args.results_dir)
    case_results = {}
    for case_name in PROMPT_CASE_ORDER:
        case_path = results_dir / f"{args.experiment_name}_{case_name}.json"
        if not case_path.is_file():
            raise FileNotFoundError(
                f"Could not find {all_cases_path} or per-case file {case_path}."
            )
        with case_path.open("r", encoding="utf-8") as handle:
            case_results[case_name] = json.load(handle)
    return case_results


def ordered_case_names(case_results: dict[str, list[dict]]) -> list[str]:
    seen = [case for case in PROMPT_CASE_ORDER if case in case_results]
    extras = [case for case in case_results if case not in PROMPT_CASE_ORDER]
    return seen + sorted(extras)


def get_metric_value(entry: dict, metric_key: str) -> float:
    if metric_key == "compressed_to_initial_ratio":
        initial_size = float(entry["original_image_size"]) + float(entry.get("original_text_size", 0.0))
        if initial_size <= 0:
            raise ValueError("Initial size must be positive to compute a compression ratio.")
        return float(entry["compressed_size"]) / initial_size
    return float(entry[metric_key])


def build_metric_values(case_results: dict[str, list[dict]], metric_key: str) -> list[list[float]]:
    values = []
    for case_name in ordered_case_names(case_results):
        values.append([get_metric_value(entry, metric_key) for entry in case_results[case_name]])
    return values


def format_case_label(case_name: str) -> str:
    return PROMPT_CASE_LABELS.get(case_name, case_name.replace("_", " ").title())


def add_jittered_points(ax, x_position: int, values: list[float], rng: random.Random, color: str) -> None:
    offsets = [rng.uniform(-0.12, 0.12) for _ in values]
    x_values = [x_position + offset for offset in offsets]
    ax.scatter(
        x_values,
        values,
        color=color,
        alpha=0.75,
        s=28,
        linewidths=0.4,
        edgecolors="white",
        zorder=3,
    )


def plot_metric(ax, case_results: dict[str, list[dict]], metric_key: str, metric_label: str, rng: random.Random) -> None:
    case_names = ordered_case_names(case_results)
    values_by_case = build_metric_values(case_results, metric_key)
    positions = list(range(1, len(case_names) + 1))

    box = ax.boxplot(
        values_by_case,
        positions=positions,
        widths=0.55,
        patch_artist=True,
        showfliers=False,
    )
    palette = ["#0b7285", "#f08c00", "#2b8a3e", "#c2255c", "#5f3dc4", "#495057"]
    for idx, patch in enumerate(box["boxes"]):
        patch.set_facecolor(palette[idx % len(palette)])
        patch.set_alpha(0.25)
        patch.set_edgecolor(palette[idx % len(palette)])
        patch.set_linewidth(1.4)
    for key in ("whiskers", "caps", "medians"):
        for line in box[key]:
            line.set_color("#343a40")
            line.set_linewidth(1.1)

    for idx, values in enumerate(values_by_case, start=1):
        color = palette[(idx - 1) % len(palette)]
        add_jittered_points(ax, idx, values, rng, color)
        mean_value = sum(values) / max(len(values), 1)
        ax.scatter(
            [idx],
            [mean_value],
            marker="D",
            s=56,
            color=color,
            edgecolors="black",
            linewidths=0.5,
            zorder=4,
        )

    ax.set_xticks(positions)
    ax.set_xticklabels([format_case_label(name) for name in case_names], rotation=15, ha="right")
    ax.set_ylabel(metric_label)
    ax.set_title(metric_label)
    if metric_key == "compressed_to_initial_ratio":
        ax.yaxis.set_major_formatter(PercentFormatter(xmax=1.0))
    ax.grid(axis="y", linestyle="--", alpha=0.35)


def build_output_path(args) -> Path:
    if args.output is not None:
        return Path(args.output)
    return Path(args.results_dir) / f"{args.experiment_name}_compression_ratio_by_case.png"


def main(argv) -> None:
    args = parse_args(argv)
    case_results = load_case_results(args)
    output_path = build_output_path(args)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    rng = random.Random(args.seed)
    fig, ax = plt.subplots(1, 1, figsize=(8, 5.5), constrained_layout=True)
    metric_key, metric_label = RATIO_METRIC
    plot_metric(ax, case_results, metric_key, metric_label, rng)
    fig.suptitle(f"{args.experiment_name}: Compression Ratio by Prompt Case", fontsize=14)
    fig.savefig(output_path, dpi=args.dpi, bbox_inches="tight")
    plt.close(fig)
    print(f"Saved plot to {output_path.resolve()}")


if __name__ == "__main__":
    main(sys.argv[1:])
